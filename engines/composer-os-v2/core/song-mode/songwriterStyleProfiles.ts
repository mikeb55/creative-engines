import type { SongwriterRuleId } from './songwritingResearchTypes';

export interface SongwriterStyleProfile {
  id: SongwriterRuleId;
  /** 0–1 — emphasis on continuous line vs riff-like cells. */
  lineVsCellEmphasis: number;
  /** 0–1 — harmonic colour vs diatonic simplicity. */
  harmonyColourBias: number;
  /** 0–1 — form regularity (high = template pop). */
  formRegularity: number;
  /** 0–1 — lyric/prosody influence on phrasing. */
  prosodyInfluence: number;
  /** 0–1 — hook repetition / return strength. */
  hookRepetitionBias: number;
  /** 0–1 — phrase regularity (high = strict 4-bar, low = irregular/through-composed). */
  phraseRegularity: number;
  /** 0–1 — hook return variation (high = transformed return, low = literal repeat). */
  hookReturnVariation: number;
  /** 0–1 — harmonic speed (high = fast changes, low = slow/static). */
  harmonicSpeed: number;
  /** 0–1 — section contrast strength (high = dramatic shifts, low = subtle). */
  sectionContrast: number;
  /** 0–1 — rhythmic syncopation bias (high = heavy syncopation, low = on-beat). */
  syncopationBias: number;
  /** 0–1 — output density (high = dense layers, low = sparse). */
  densityBias: number;
  /** Typical motif cell size in notes (2–7). */
  motifCellSize?: number;
  /** Interval preference tag for motif generation. */
  intervalPreference?: 'stepwise' | 'triadic' | 'angular' | 'chromatic' | 'modal' | 'pentatonic' | 'mixed';
  /** Guitar lead interval preference tag. */
  guitarLeadIntervals?: string;
  /** Primary modal preference for guitar. */
  guitarModalPreference?: string;
}

